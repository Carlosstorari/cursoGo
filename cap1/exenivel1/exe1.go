package exenivel1

import (
	"fmt"
)

func Exe1() {
	x := 42
	y := "James Bond"
	z := true

	fmt.Printf("x = %v, y = %v, z = %v\n", x, y, z)
	fmt.Println("x = ", x)
	fmt.Println("y = ", y)
	fmt.Println("z = ", z)
}

